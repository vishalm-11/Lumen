export const ALTITUDE_MEDIUM_START = 17_000_000
export const ALTITUDE_MEDIUM_END = 15_000_000
export const ALTITUDE_SMALL_START = 9_000_000
export const ALTITUDE_SMALL_END = 7_000_000

const GOLDEN_ANGLE = 2.399963229728653

function distanceDegrees([lat1, lng1], [lat2, lng2]) {
  return Math.hypot(lat1 - lat2, lng1 - lng2)
}

function smoothstep(edge0, edge1, x) {
  const t = Math.min(1, Math.max(0, (x - edge0) / (edge1 - edge0)))
  return t * t * (3 - 2 * t)
}

export function spreadOverlappingMarkers(centroids, minDistance = 1.05) {
  const names = Object.keys(centroids).sort()
  const adjusted = {}
  names.forEach((name, index) => {
    const [baseLat, baseLng] = centroids[name]
    let lat = baseLat
    let lng = baseLng
    for (let attempt = 0; attempt < 12; attempt += 1) {
      const tooClose = Object.values(adjusted).some((coords) =>
        distanceDegrees([lat, lng], coords) < minDistance
      )
      if (!tooClose) break
      const angle = index * GOLDEN_ANGLE + attempt * 0.9
      const radius = minDistance * (0.55 + attempt * 0.22)
      lat = baseLat + radius * Math.sin(angle)
      lng = baseLng + radius * Math.cos(angle)
    }
    adjusted[name] = [lat, lng]
  })
  return adjusted
}

export const COUNTRY_CENTROIDS = spreadOverlappingMarkers(COUNTRY_CENTROIDS_RAW)

export function getTierVisibility(tier, altitude) {
  if (tier === "large") return 1
  if (tier === "medium") {
    return 1 - smoothstep(ALTITUDE_MEDIUM_END, ALTITUDE_MEDIUM_START, altitude)
  }
  return 1 - smoothstep(ALTITUDE_SMALL_END, ALTITUDE_SMALL_START, altitude)
}

export function calculateMarkerDensity(lat, lng, centroids = COUNTRY_CENTROIDS) {
  return Object.values(centroids).reduce((count, [otherLat, otherLng]) => {
    const dist = Math.hypot(lat - otherLat, lng - otherLng)
    return dist < 5 ? count + 1 : count
  }, 0)
}

export function getMarkerStyle(lat, lng, centroids = COUNTRY_CENTROIDS) {
  const density = calculateMarkerDensity(lat, lng, centroids)
  if (density > 6) return { pixelSize: 9, outlineWidth: 6, alpha: 1 }
  if (density > 4) return { pixelSize: 8, outlineWidth: 5, alpha: 1 }
  if (density > 2) return { pixelSize: 7, outlineWidth: 5, alpha: 1 }
  return { pixelSize: 6, outlineWidth: 5, alpha: 1 }
}
